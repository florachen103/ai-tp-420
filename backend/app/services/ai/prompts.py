def ask_llm_config(response_style: str, *, has_persona: bool = False) -> tuple[str, int]:
    """返回 (system_prompt, max_tokens)。含学员画像时略增字数；标准/深度模式默认含划重点与引用话术块。"""
    if response_style == "deep":
        return SYSTEM_TUTOR_DEEP, 1800 if has_persona else 1700
    if response_style == "standard":
        return SYSTEM_TUTOR, 1100 if has_persona else 1000
    return SYSTEM_TUTOR_MICRO, 560 if has_persona else 520


